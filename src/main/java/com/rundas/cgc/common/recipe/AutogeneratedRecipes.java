    public static void init(Consumer<FinishedRecipe> provider) {
        for (Material material : MaterialRegistryManager.getInstance().getRegisteredMaterials()) {
            if (!material.hasProperty(CGCPropertyKeys.PHYSICS)) {
                continue;
            }
            int tier = material.getProperty(CGCPropertyKeys.PHYSICS).tier();
            if (material.hasProperty(CGCPropertyKeys.SOLUTION)) {
                processSolutions(provider, material, tier);
            }
            if (material.hasFlag(CGCMaterialFlags.IS_ROASTED)) {
                processRoastedDustDecomposition(provider, material, tier);
            }
            if (material.hasProperty(CGCPropertyKeys.NUCLEAR)) {
                processNuclearRecipes(provider, material, tier);
            }
        }
    }

    private static void processNuclearRecipes(Consumer<FinishedRecipe> provider, Material material, int tier) {
        NuclearProperty property = material.getProperty(CGCPropertyKeys.NUCLEAR);
        processIsotopicStabilizerRecipe(provider, material, property, tier);
        processDecayHastenerRecipes(provider, material, property);
        processDecayGeneratorRecipes(provider, material, property, tier);
        if (property.isFuel()) {
            List<Tuple<List<Material>, Double>> reactions = getFissionReactions(material);
            processFissionReactorRecipes(provider, material, property, tier, reactions);
            // processFuelReprocessorRecipes(provider, material, property, tier, reactions);
        }
    }

    private static void processFissionReactorRecipes(Consumer<FinishedRecipe> provider, Material material,
                                                     NuclearProperty property, int tier,
                                                     List<Tuple<List<Material>, Double>> fissionReactions) {
        CompoundTag tag = new CompoundTag();
        for (int i = 0; i < fissionReactions.size(); i++) {
            tag.putInt("recipe", i);
            FISSION_REACTOR_RECIPES.recipeBuilder("fissioning_" + material.getUnlocalizedName() + "way_" + (i + 1))
                    .inputItems(nuclearFuel, material)
                    .circuitMeta(i)
                    .outputItems(new ItemStack(ChemicalHelper.get(depletedNuclearFuel, material).getItem(), 1, tag))
                    .duration((int) Math.round(fissionReactions.get(i).getB())).save(provider);
            tag.remove("recipe");
        }
    }

    private static void processIsotopicStabilizerRecipe(Consumer<FinishedRecipe> provider, Material material,
                                                        NuclearProperty property, int tier) {
        Material stable = property.stableMaterial();
        int neutrons = Math.toIntExact(material.getNeutrons() - stable.getNeutrons());
        if (material.hasProperty(PropertyKey.DUST)) {
            if (stable.hasProperty(PropertyKey.DUST)) {
                if (neutrons > 0) {
                    ISOTOPIC_STABILIZER_RECIPES.recipeBuilder("stabilizing_" + material.getUnlocalizedName())
                            .inputItems(dust, material)
                            .outputItems(dust, stable)
                            .outputFluids(FreeNeutronGas.getFluid(neutrons))
                            .EUt(GTValues.VA[tier]).duration((int) material.getMass()).save(provider);
                } else {
                    ISOTOPIC_STABILIZER_RECIPES.recipeBuilder("stabilizing_" + material.getUnlocalizedName())
                            .inputItems(dust, material)
                            .inputFluids(FreeNeutronGas.getFluid(neutrons))
                            .outputItems(dust, stable)
                            .EUt(GTValues.VA[tier]).duration((int) material.getMass()).save(provider);
                }
            } else {
                if (neutrons > 0) {
                    ISOTOPIC_STABILIZER_RECIPES.recipeBuilder("stabilizing_" + material.getUnlocalizedName())
                            .inputItems(dust, material)
                            .outputFluids(stable.getFluid(1000), FreeNeutronGas.getFluid(neutrons))
                            .EUt(GTValues.VA[tier]).duration((int) material.getMass()).save(provider);
                } else {
                    ISOTOPIC_STABILIZER_RECIPES.recipeBuilder("stabilizing_" + material.getUnlocalizedName())
                            .inputItems(dust, material)
                            .inputFluids(FreeNeutronGas.getFluid(neutrons))
                            .outputFluids(stable.getFluid(1000))
                            .EUt(GTValues.VA[tier]).duration((int) material.getMass()).save(provider);
                }
            }
        } else {
            if (stable.hasProperty(PropertyKey.DUST)) {
                if (neutrons > 0) {
                    ISOTOPIC_STABILIZER_RECIPES.recipeBuilder("stabilizing_" + material.getUnlocalizedName())
                            .inputFluids(material.getFluid(1000))
                            .outputItems(dust, stable)
                            .outputFluids(FreeNeutronGas.getFluid(neutrons))
                            .EUt(GTValues.VA[tier]).duration((int) material.getMass()).save(provider);
                } else {
                    ISOTOPIC_STABILIZER_RECIPES.recipeBuilder("stabilizing_" + material.getUnlocalizedName())
                            .inputFluids(material.getFluid(1000), FreeNeutronGas.getFluid(neutrons))
                            .outputItems(dust, stable)
                            .EUt(GTValues.VA[tier]).duration((int) material.getMass()).save(provider);
                }
            } else {
                if (neutrons > 0) {
                    ISOTOPIC_STABILIZER_RECIPES.recipeBuilder("stabilizing_" + material.getUnlocalizedName())
                            .inputFluids(material.getFluid(1000))
                            .outputFluids(stable.getFluid(1000), FreeNeutronGas.getFluid(neutrons))
                            .EUt(GTValues.VA[tier]).duration((int) material.getMass()).save(provider);
                } else {
                    ISOTOPIC_STABILIZER_RECIPES.recipeBuilder("stabilizing_" + material.getUnlocalizedName())
                            .inputFluids(material.getFluid(1000), FreeNeutronGas.getFluid(neutrons))
                            .outputFluids(stable.getFluid(1000))
                            .EUt(GTValues.VA[tier]).duration((int) material.getMass()).save(provider);
                }
            }
        }
    }

