    private static void processRoastedDustDecomposition(Consumer<FinishedRecipe> provider, Material material,
                                                        int tier) {
        CENTRIFUGE_RECIPES.recipeBuilder("decomposing_" + material.getUnlocalizedName())
                .inputItems(dust, material, getMolarAmount(material))
                .outputItems(material.getMaterialComponents())
                .inputEU(GTValues.VA[tier]).duration((int) material.getMass()).save(provider);
    }

    private static void processSolutions(Consumer<FinishedRecipe> provider, Material material, int tier) {
        SolutionProperty property = material.getProperty(CGCPropertyKeys.SOLUTION);
        processSolutionMixing(provider, material, property, tier);
        processSolutionCrystallization(provider, property.solution(), property.crystallizedSolution(), tier);
        processSolutionDehydration(provider, material, property.crystallizedSolution(), tier);
    }

    private static void processSolutionMixing(Consumer<FinishedRecipe> provider, Material material,
                                              SolutionProperty property, int tier) {
        final Material solvent = property.solvent();
        String name = "mixing_" + material.getUnlocalizedName() + solvent.getUnlocalizedName() + "_solution";

        processRegularSolutionMixing(provider, material, solvent, property.solution(), tier, name);

        for (int i = 0; i < property.dilutedSolutions().size(); i++) {
            if (i == 0) {
                name = "mixing_diluted_" + material.getUnlocalizedName() + solvent.getUnlocalizedName() + "_solution";
                process1XDilutedSolutionMixing(provider, material, solvent, property.solution(),
                        property.dilutedSolutions().get(0), tier, name);
            } else {
                name = "mixing_" + i + "x_diluted_" + material.getUnlocalizedName() +
                        solvent.getUnlocalizedName() + "_solution";
                processFurtherDilutedSolutionMixing(provider, material, solvent, property.dilutedSolutions().get(i),
                        property.dilutedSolutions(), i, tier, name);
            }
        }
    }

    private static void processRegularSolutionMixing(Consumer<FinishedRecipe> provider, Material material,
                                                     Material solvent, Material solution, int tier, String name) {
        MIXER_RECIPES.recipeBuilder(name)
                .inputItems(dust, material, getMolarAmount(material))
                .circuitMeta(1)
                .inputFluids(solvent.getFluid(1000))
                .outputFluids(solution.getFluid(1000))
                .inputEU(GTValues.VA[tier]).duration(40).save(provider);
    }

    private static void process1XDilutedSolutionMixing(Consumer<FinishedRecipe> provider, Material material,
                                                       Material solvent, Material concentratedSolution,
                                                       Material dilutedSolution, int tier, String name) {
        MIXER_RECIPES.recipeBuilder(name)
                .inputItems(dust, material)
                .circuitMeta(2)
                .inputFluids(solvent.getFluid(2000))
                .outputFluids(dilutedSolution.getFluid(2000))
                .inputEU(GTValues.VA[tier]).duration(40).save(provider);

        MIXER_RECIPES.recipeBuilder(name)
                .circuitMeta(2)
                .inputFluids(concentratedSolution.getFluid(1000), solvent.getFluid(1000))
                .outputFluids(dilutedSolution.getFluid(2000))
                .inputEU(GTValues.VA[tier]).duration(40).save(provider);
    }

    private static void processFurtherDilutedSolutionMixing(Consumer<FinishedRecipe> provider, Material material,
                                                            Material solvent, Material solution,
                                                            List<Material> solutions, int tier, int i, String name) {
        MIXER_RECIPES.recipeBuilder(name)
                .inputItems(dust, material)
                .circuitMeta(i)
                .inputFluids(solvent.getFluid(i * 1000))
                .outputFluids(solution.getFluid(i * 1000))
                .inputEU(GTValues.VA[tier]).duration(i * 20).save(provider);

        for (int j = 1; j <= i - 1; j++) {
            MIXER_RECIPES.recipeBuilder(name)
                    .circuitMeta(j)
                    .inputFluids(solutions.get(i - j).getFluid(1000), solvent.getFluid(j * 1000))
                    .outputFluids(solution.getFluid(i * 1000))
                    .inputEU(GTValues.VA[tier]).duration(j * 20).save(provider);
        }
    }

    private static void processSolutionCrystallization(Consumer<FinishedRecipe> provider, Material solution,
                                                       Material crystallizedSolution, int tier) {
        CRYSTALLIZER_RECIPES.recipeBuilder("crystallizing_" + solution.getUnlocalizedName())
                .inputFluids(solution.getFluid(1000))
                .outputFluids(crystallizedSolution.getFluid(1000))
                .inputEU(GTValues.VA[tier]).duration((int) solution.getMass()).save(provider);
    }

    private static void processSolutionDehydration(Consumer<FinishedRecipe> provider, Material material,
                                                   Material solution, int tier) {
        DEHYDRATOR_RECIPES.recipeBuilder("dehydrating_" + solution.getUnlocalizedName())
                .inputFluids(solution.getFluid(1000))
                .outputItems(dust, material, getMolarAmount(material))
                .inputEU(GTValues.VA[tier]).duration((int) solution.getMass()).save(provider);
    }
